"""
List Methods
"""

numbers = [10, 20, 30]

numbers.append(40)
print("Append:", numbers)

numbers.insert(1, 15)
print("Insert:", numbers)

numbers.remove(20)
print("Remove:", numbers)

numbers.pop()
print("Pop:", numbers)

numbers.sort()
print("Sort:", numbers)

numbers.reverse()
print("Reverse:", numbers)